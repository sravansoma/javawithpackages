package regularExamples;

public class IfCondition {
    public static void main (String[] args) {
        int x = 2;
        if (x == 3) {
            System.out.println("Yes it is 3");
        } else {
             System.out.println("It is not 3");
        }
        System.out.println("This condition will always be true");
    }
}